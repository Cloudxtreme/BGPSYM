package nl.nlnetlabs.bgpsym01.primitives;

public class BGPSymException extends RuntimeException {

    /**
     * 
     */
    private static final long serialVersionUID = 384380591558213913L;

    public BGPSymException() {
        super();
    }

    public BGPSymException(String message, Throwable cause) {
        super(message, cause);
    }

    public BGPSymException(String message) {
        super(message);
    }

    public BGPSymException(Throwable cause) {
        super(cause);
    }

}
